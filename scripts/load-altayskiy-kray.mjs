async function main() {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !key) {
    console.error("Нет ключей Supabase в .env.local");
    process.exit(1);
  }
  const supabase = createClient(url, key, {
    auth: { autoRefreshToken: false, persistSession: false },
  });

  const slugs = measures.map((m) => m.slug);
  const { data: existing } = await supabase
    .from("measures")
    .select("slug")
    .in("slug", slugs);
  const had = new Set((existing ?? []).map((r) => r.slug));

  const { error } = await supabase
    .from("measures")
    .upsert(measures, { onConflict: "slug" });
  if (error) {
    console.error("Ошибка записи: " + error.message);
    process.exit(1);
  }

  const created = slugs.filter((s) => !had.has(s)).length;
  const published = measures.filter((m) => m.is_published).length;
  console.log(
    `${R}: обработано ${measures.length} (создано ${created}, обновлено ${measures.length - created})`,
  );
  console.log(`  опубликовано: ${published}, черновиков: ${measures.length - published}`);
  for (const m of measures) {
    console.log(`  ${m.is_published ? "✓ опубл." : "✎ черновик"}  ${m.slug} — ${m.amount}`);
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
