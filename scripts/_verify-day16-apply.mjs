const APPLY = process.argv.includes("--apply");
const slugs = Object.keys(PATCHES);

const { data: current, error } = await sb
  .from("measures")
  .select("*")
  .in("slug", slugs);
if (error) {
  console.error("Ошибка чтения:", error.message);
  process.exit(1);
}
const bySlug = Object.fromEntries(current.map((r) => [r.slug, r]));

// Дифф
let changes = 0;
const merged = [];
for (const slug of slugs) {
  const cur = bySlug[slug];
  if (!cur) {
    console.log(`⚠ НЕ НАЙДЕНА: ${slug}`);
    continue;
  }
  const patch = PATCHES[slug];
  const row = { ...cur, ...patch };
  merged.push(row);
  console.log(`\n### ${slug}`);
  for (const k of Object.keys(patch)) {
    const before = JSON.stringify(cur[k]);
    const after = JSON.stringify(patch[k]);
    if (before !== after) {
      changes++;
      console.log(`  ${k}:`);
      console.log(`    − ${before}`);
      console.log(`    + ${after}`);
    }
  }
}
console.log(`\nИтого изменений полей: ${changes} по ${slugs.length} мерам`);

if (!APPLY) {
  console.log("\nСухой прогон. Для записи: node scripts/_verify-day16-apply.mjs --apply");
  process.exit(0);
}

// Бэкап и запись
const stamp = process.argv.find((a) => a.startsWith("--stamp="))?.slice(8) || "day16";
const backupFile = `verification/backup-${stamp}.json`;
writeFileSync(backupFile, JSON.stringify(current, null, 2));
console.log(`\nБэкап текущих строк: ${backupFile}`);

let ok = 0, fail = 0;
for (const row of merged) {
  const { error } = await sb.from("measures").upsert(row, { onConflict: "slug" });
  if (error) { console.log("FAIL", row.slug, "-", error.message); fail++; }
  else { console.log("OK  ", row.slug); ok++; }
}
console.log(`\nDONE: ${ok} обновлено, ${fail} ошибок`);
