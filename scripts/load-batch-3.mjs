async function main() {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !key) { console.error("Нет ключей Supabase"); process.exit(1); }
  const supabase = createClient(url, key, { auth: { autoRefreshToken: false, persistSession: false } });

  // Новая мера
  const { data: had } = await supabase.from("measures").select("slug").eq("slug", newMeasure.slug);
  const { error: insErr } = await supabase.from("measures").upsert(newMeasure, { onConflict: "slug" });
  if (insErr) console.error(`Новая мера: ошибка — ${insErr.message}`);
  else console.log(`Новая мера ${newMeasure.slug}: ${had && had.length ? "обновлена" : "добавлена (черновик)"}`);

  // Обновления существующих
  for (const u of updates) {
    const { data, error } = await supabase.from("measures").update(u.patch).eq("slug", u.slug).select("slug");
    if (error) console.error(`${u.slug}: ошибка — ${error.message}`);
    else if (!data || data.length === 0) console.warn(`${u.slug}: меры нет — пропущено`);
    else console.log(`${u.slug}: обновлено (${Object.keys(u.patch).join(", ")})`);
  }
}

main().catch((e) => { console.error(e); process.exit(1); });
