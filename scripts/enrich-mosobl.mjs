async function main() {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !key) { console.error("Нет ключей Supabase в .env.local"); process.exit(1); }
  const supabase = createClient(url, key, { auth: { autoRefreshToken: false, persistSession: false } });

  let ok = 0, missing = 0, pub = 0, draft = 0;
  for (const u of updates) {
    const { is_published, ...fields } = u;
    const { data, error } = await supabase
      .from("measures")
      .update({ ...fields, is_published, updated_at_label: "2026" })
      .eq("slug", u.slug)
      .select("slug");
    if (error) { console.error(`✗ ${u.slug}: ${error.message}`); continue; }
    if (!data || data.length === 0) { console.warn(`? ${u.slug}: запись НЕ найдена (пропущена)`); missing++; continue; }
    ok++; is_published ? pub++ : draft++;
    console.log(`${is_published ? "✓ опубл." : "✎ черновик"}  ${u.slug} — ${u.amount ?? "—"}`);
  }
  console.log(`\n${R}: обновлено ${ok} (опубликовано ${pub}, черновиков ${draft}), не найдено ${missing}`);
}

main().catch((e) => { console.error(e); process.exit(1); });
